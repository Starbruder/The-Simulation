namespace TheSimulation;

public enum BeaufortScale
{
    Calm = 0,               // Windstille
    LightAir = 1,           // Leichter Luftzug
    LightBreeze = 2,        // Leichte Brise
    GentleBreeze = 3,       // Mäßige Brise
    ModerateBreeze = 4,     // Frische Brise
    FreshBreeze = 5,        // Starker Wind
    StrongBreeze = 6,       // Sehr starker Wind
    Gale = 7,               // Stürmischer Wind
    SevereGale = 8,         // Sehr stürmisch
    Storm = 9,              // Orkanartiger Wind
    ViolentStorm = 10,      // Orkan
    Hurricane = 11          // Sehr starker Orkan
}
